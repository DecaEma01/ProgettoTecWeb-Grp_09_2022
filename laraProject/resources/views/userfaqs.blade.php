    <h1>F.A.Q</h1>
    <h2>Come posso pubblicare un annuncio? E quanto costa?</h2>

    <p>Per pubblicare il tuo annuncio devi prima registrarti gratuitamente su HouseFinder.<br>
    Successivamente potri inserire il tuo immobile direttamente dal sito.<br>
    Tutto ciò è assolutamente gratuito, così è e così sarà in futuro in quanto nostro principio cardine, offire un ottimo servizio completamente gratuito.</p>

    <h2>Come posso modificare e/o cancellare un annuncio che ho pubblicato?</h2>

    <p>Per modificare o cancellare un annuncio da HouseFinder, accedi all'area personale inserendo indirizzo email e password scelti al momento della registrazione.<br>
    Una volta effettuato il login, seleziona la voce "Le mie Case".<br>Cliccando sull'icona a forma di penna vicino all'inserzione, è possibile modificare l'annuncio, 
    o cliccando sull'icona a forma di cesitono portà eliminare l'annuncio.</p>

    <h2>Posso sapere quante volte viene visualizzato il mio annuncio al giorno?</h2>

    <p>Attualmente questa funzione non è ancora disponibile, ma lo sarà sicuramente nei prossimi mesi.</p>
