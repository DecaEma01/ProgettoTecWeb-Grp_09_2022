    <h2>Come posso modificare e/o cancellare un annuncio che ho pubblicato?</h2>

    <p>Per modificare o cancellare un annuncio da HouseFinder, accedi all'area personale inserendo indirizzo email e password scelti al momento della registrazione.<br>
    Una volta effettuato il login, seleziona la voce "Le mie Case".<br>Cliccando sull'icona a forma di penna vicino all'inserzione, è possibile modificare l'annuncio, 
    o cliccando sull'icona a forma di cesitono portà eliminare l'annuncio.</p>

